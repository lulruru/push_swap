long	ft_atol(char *str)
{
	int	i;
	int sign;
	int long r;

	i = 0;
	sign = 0;
	r = 0;
	if (str[i]  == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - 48;
		i++;
	}
	return (r * sign);
}